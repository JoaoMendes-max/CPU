`timescale 1ns / 1ps
`default_nettype none

module tb_soc_simple;

/*************************************************************************************
 * SECTION 1. DECLARE WIRES / REGS
 ************************************************************************************/
    reg _clk = 1'b0;
    reg _rst = 1'b1;

/*************************************************************************************
 * SECTION 2. IMPLEMENTATION
 ************************************************************************************/

/*************************************************************************************
 * 2.1 DUT instantiation and clock generation
 ************************************************************************************/
    soc dut (
        .i_clk      (_clk),
        .i_rst      (_rst),
        .i_par_i    (4'h0),
        .o_par_o    (),
        .i_uart_rx  (1'b1),
        .o_uart_tx  (),
        .io_i2c_sda (),
        .io_i2c_scl ()
    );

    always #5 _clk = ~_clk;

/*************************************************************************************
 * 2.2 Reset sequence
 ************************************************************************************/
    task wait_clocks(input integer n);
        repeat (n) @(posedge _clk);
    endtask

    initial begin
        wait_clocks(12);
        _rst = 1'b0;
    end

/*************************************************************************************
 * 2.3 Cycle guard
 ************************************************************************************/
    integer _cycles = 0;
    always @(posedge _clk) begin
        if (!_rst) _cycles <= _cycles + 1;
    end

    initial begin
        wait (_rst == 1'b0);
        wait (_cycles >= 200);
        $display("Simulation done at cycles=%0d", _cycles);
        $finish;
    end

/*************************************************************************************
 * 2.4 Waveform dump
 ************************************************************************************/
    initial begin
        $dumpfile("waves_soc_simple.vcd");
        $dumpvars(0, tb_soc_simple);
    end

endmodule